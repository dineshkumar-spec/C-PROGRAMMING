//8. Write a program to demonstrate the use of int, float, char, and double data types
#include <stdio.h>
int main(){
  int num1=100;
  float num2=7.5;
  double num3=59.9;
  char name[]="Laptop";
  printf("Enter num1:%d\n",num1);
  printf("Enter num2:%f\n",num2);
  printf("Enter num3:%lf\n",num3);
  printf("name:%s\n",name);
  return 0;
}
